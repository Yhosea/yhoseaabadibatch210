public class jajar {
    public static void main(String[] args) {
        for (int i = 0; i<10; i++){
            for (int k = 10; k>i; k--){
                System.out.print(" ");
            }
            for (int j = 0; j<15; j++){
                System.out.print(" * ");
            }
            System.out.println("");
        }
    }
}
